int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	differ;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			differ = s1[i] - s2[i];
			return (differ);
		}
		i++;
	}
	return (0);
}
