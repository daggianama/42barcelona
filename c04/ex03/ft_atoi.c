int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	s;

	s = 1;
	n = 0;
	i = 0;
	while (str[i] == '\f' || str[i] == '\t' || str[i] == '\v')
		i++;
	while (str[i] == '\r' || str[i] == ' ' || str[i] == '\n')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = s * -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * s);
}
