#include "libft.h"

void	*ft_memchr(const void *s, int a, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)a)
			return ((void *)&s[i]);
		i++;
	}
	return (0);
}
